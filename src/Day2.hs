solveA :: [String] -> Int
solveA x = amountOfDubbel x * amountOfTripel x


solveB :: [String] -> String
solveB x = maximumBy (comparing length) $ map (foldIntersect x) x


amountOfDubbel :: [String] -> Int
amountOfDubbel = foldl' (\acc x -> acc + dubbel x) 0
                  where
                    dubbel x
                            | (any ((==2) . length) . group . sort) x = 1
                            | otherwise = 0


amountOfTripel :: [String] -> Int
amountOfTripel = foldl' (\acc x -> acc + tripel x) 0
                  where
                    tripel x
                            | (any ((==3) . length) . group . sort) x = 1
                            | otherwise = 0


foldIntersect :: [String] -> String -> String
foldIntersect xs y = foldl' (longestIntersect y) "" xs


longestIntersect :: String -> String -> String -> String
longestIntersect x y z
                      | length intersect > length y && length intersect < length x = intersect
                      | otherwise = y
                      where
                        intersect = charsIntersect x z


charsIntersect :: String -> String -> String
charsIntersect [] _           = []
charsIntersect (x:xs) (y:ys)
                            | x == y    = x : charsIntersect xs ys
                            | otherwise = charsIntersect xs ys


